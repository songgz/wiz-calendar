    parseTime() {
        let t = this.day;
        this.day = Math.floor(this.day);
        t = (t - this.day) * 24;
        this.hour = Math.floor(t);
        t = (t - this.hour) * 60;
        this.minute = Math.floor(t);
        this.second = Math.floor((t - this.minute) * 60);
    }

    getTimeToJD() {
        return (this.hour * 3600 + this.minute * 60 + this.second) / 86400.0
    }

