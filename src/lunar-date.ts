    calcMoonPhase111() {
        let ms = SunMoon.aLongD((this.getFirstOfMonth() ) / 36525, 10, 3);
        ms = Math.floor((ms + 2) / Angle.PI2) * Angle.PI2; //定朔计算月初
        let phaseRad = Angle.PI2 / 4.0;
        let signs = ['●', '☽', '○', '☾'];
        let phase = 0;
        for (let j = 0; j < 4; j++) {
            phase = SunMoon.mjd(ms + phaseRad * j);
            this.moonPhases[Math.floor(phase + 0.5) - this.getFirstOfMonth() + 1] = {
                mjd: phase,
                phase: j + 1,
                sign: signs[j],
                time: JulianDate.timeStr(phase)
            };
        }
        return this.moonPhases;
    }

    //返回jdnUTC
    getFirstOfMonth(): number {
        return this.getJulianDate().mjdn() - this.day + 1;
    }

    getLastOfMonth() {
        let ms = SunMoon.aLongD((this.getFirstOfMonth()) / 36525, 10, 3);
        ms = Math.floor((ms + 2) / Angle.PI2) * Angle.PI2;
        return Math.floor(SunMoon.mjd(ms + Angle.PI2) + 0.5) - 1;
    }

    calcPentads() {
        let first = this.getFirstOfMonth();
        let w = Sun.aLong((first) / 36525, 3);
        //nCF = int2(w / pi2 + 0.01);
        let pentadRad = Angle.PI2 / 72;
        w = Math.floor(w / pentadRad) * pentadRad;
        while (Math.floor(Sun.mjd(w) + 0.5) < Math.floor(first + 0.5)) {
            w += pentadRad;
        }
        let wn = (Math.floor((w + 0.02) / pentadRad) % 72 + 72) % 72;
        for (let j = 0; j <= 6; j++) {
            let mjd = Sun.mjd(w + j * pentadRad);
            let pentad = {
                mjd: mjd,
                idx: (wn + j) % 72,
                name: '',
                sign: '',
                style: 0,
                time: JulianDate.timeStr(mjd)
            };
            switch (pentad.idx % 3) {
                case 2:
                    pentad.name = '三候';
                    pentad.style = 3;
                    pentad.sign = '▲';
                    break;
                case 0:
                    //pentad.name = LunarDate.SolarTerms[(((wn + j) / 3) % 24 + 24) % 24];
                    //pentad.style = ((((wn + j) / 3) % 24 + 6) % 24) % 2;
                    pentad.name = LunarDate.SolarTerms[(pentad.idx / 3) % 24];
                    pentad.style = (pentad.idx / 3) % 24 % 2;
                    pentad.sign = '◆';
                    break;
                case 1:
                    pentad.name = '二候';
                    pentad.style = 2;
                    pentad.sign = '▲';
                    break;
            }
            this.pentads[Math.floor(pentad.mjd + 0.5) - first + 1] = pentad;
        }
        return this.pentads;
    }

